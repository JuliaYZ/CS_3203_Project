function comingSoon() {
    var x = document.getElementById("CS");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function xCS() {
    var x = document.getElementById("CS");
    x.style.display === "none";
}

function myProfile() {
    var x = document.getElementById("pro");
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}
